package com.sujalchoudhari;

public class NOTES_08_Loops {

	public static void main(String[] args) {
		int i =0;
		//while
		while(i <=10) {
			System.out.println(i);
			++i;
		}
		
		//for
		for(i=10;i<=20;i++) {
			System.out.println(i);
		}
		
		// do while
		do {
			System.out.println(i);
			++i;
		}while(i <=30);
	}

}
