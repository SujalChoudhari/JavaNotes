package com.sujalchoudhari;

public class NOTES_07_SwitchCases {
	public static void main(String[] args) {
		char character = 'c';

		switch (character) {
		case 'a':
			System.out.println("Yes it is a");
			break;
		case 'b':
			System.out.println("Yes it is b");
			break;
		default:
			System.out.println("IDK what it is");

		}
	}
}
